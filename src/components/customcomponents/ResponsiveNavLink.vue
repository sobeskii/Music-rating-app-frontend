<template>
    <div>
        <button :class="classes" class="w-full text-left" v-if=" asx == 'button' ">
            <slot></slot>
        </button>

        <router-link :to="href" :class="classes" v-else>
            <slot></slot>
        </router-link>
    </div>
</template>

<script>
    export default {
        props: ['active', 'href', 'asx'],

        computed: {
            classes() {
                return this.active
                            ? 'block pl-3 pr-4 py-2 border-l-4 border-green-300 text-base font-medium text-green-500 bg-green-50 focus:outline-none focus:text-green-500 focus:bg-green-100 focus:border-green-800 transition duration-150 ease-in-out'
                            : 'block pl-3 pr-4 py-2 border-l-4 border-transparent text-base font-medium text-white hover:text-gray-800 hover:bg-gray-50 hover:border-gray-300 focus:outline-none focus:text-gray-800 focus:bg-gray-50 focus:border-gray-300 transition duration-150 ease-in-out'
            }
        }
    }
</script>
